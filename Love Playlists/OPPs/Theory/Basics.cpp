/*
Princliples of Object Oriented Programming

Abstraction - the process of hiding the internal details of an application from the outer world. Abstraction is used to describe things in simple terms.
It's used to create a boundary between the application and the client programs.

Encapsulation - the bundling of data, along with the methods that operate on that data, into a single unit .
Data hiding is an object-oriented programming (OOP) technique specifically used to hide internal object details (i.e., data members). Data hiding guarantees exclusive data access to class members only and protects and maintains object integrity by preventing intended or unintended changes and intrusions.

Inheritance - the concept in OOPs in which one class inherits the attributes and methods of another class. The class whose properties and methods are inherited is known as the Parent class. And the class that inherits the properties from the parent class is the Child class.

Polymorphism - a feature of object-oriented programming languages that allows a specific routine to use variables of different types at different times. Polymorphism in programming gives a program the ability to redefine methods for derived classes.
*/

